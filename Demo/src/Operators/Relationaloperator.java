package Operators;

public class Relationaloperator {
	
public static void main(String[]args) {
		
		//variable declaration
		
		int a = 20,b = 20;
		
		//value of a and b
		
		System.out.println("a is"+a + "b is"+b);
		
		// == operater
		System.out.println(a==b);
		
		//!= operator
		System.out.println(a!=b);
		
		//> operator
		System.out.println(a>b);
		
		//< operator
		System.out.println(a<b);
		
		//>= operator
		System.out.println(a>=b);
		
		//<= operator
		System.out.println(a<=b);
		
		
		
		
	}
	


}
