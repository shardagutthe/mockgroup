package Operators;

public class Logicaloperator {
	
public static void main(String[]args) {
		
		//&&Operator 
		
		System.out.println((10>5)&&(12>3));
		
		System.out.println((4>5)&&(12>3));
		
		// || operator
		System.out.println((10>5)||(12>3));
				
		System.out.println((10<5)||(12<3));
		
		//! operator(not)

		System.out.println(!(10>5));
		
		System.out.println(!(5<5));
		

}

}
