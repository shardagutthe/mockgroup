package assignmentarray;

import java.util.Arrays;

public class DESCusingwhile {  //desending order using whileloop
	
	public static void main(String[] args) {
		int arr[]= {10,20,30,70,50,60};
     	Arrays.sort(arr);
    	int j =arr.length-1;
		while(j>=0) {
			System.out.print(arr[j]+",");
			j--;
}
	}
}
			
		
		
		
		
		
		
		
		


