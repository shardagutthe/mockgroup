package DataTypes;

public class DataTypess {
	
public static void main(String[]args) {
		
		boolean stdPass = true; //conditional datatypes only TRUE or FALSE output
		
		byte stdMaxMarks = 100; //values from -128 to 127 insted of int to save memory
		
		short stdTotalMarks = 32767;//values from -32768 to 32767
		
		int stdRollNo = 431509;//greater than short values
		
		long stdMobNo = 9765686001L;//L is mandatory when we declare long
		
		double stdAvgMarks = 90.878;//use for fractional no max 15digit
		
		float stdPercentage= 99.65f;//f is mandatory
		
		char stdGrade = 'A';
		
		String StdName = "SHARNIL";
		
		System.out.println(stdPass);
		System.out.println(stdMaxMarks);
		System.out.println(stdTotalMarks);
		System.out.println(stdRollNo);
		System.out.println(stdMobNo);
		System.out.println(stdAvgMarks);
		System.out.println(stdPercentage);
		System.out.println(stdGrade);
		System.out.println(StdName);


	}

}



